int	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	return (0);
}

//   The values returned are nonzero if the character c falls into the
//    tested class, and zero if not.